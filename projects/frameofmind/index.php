				<h1>Frame of Mind</h1>
				<h3>Fall 2012 | openFrameworks, AfterEffects</h3>
				<p>How do you feel? It's a seemingly innocuous question, but it shouldn't be. We often pose this question to others, or are asked it ourselves, without ever contemplating a genuine answer. The metaphysical question of how we "feel" is usually brushed away as a casual hello. Why is this the case? Feelings, mood, and emotion have serious implications in our relationships, our work, and our health, even if they aren't something we can empirically measure or substantiate. In a modern, fast-paced society, we have grown accustomed to suppressing these emotions and disregarding our mood. Has time become too precious, too scarce to justify more than a passing thought of our emotional wellbeing?</p>
				<p>Traditionally, different types of media have served as a surface on which to project your own thoughts, feelings, and memories. Artistic mediums such as film and music have often tried to project a specific mood or ideal to their audiences. Upon experiencing the work, an individual is supposed to feel or think a certain way, and oftentimes they do. Visual and auditory media hold a powerful sway over our imagination. </p>
				<p>What I find more interesting, however, is quite the opposite; how can the mood and emotion of an individual control and inform the content of what they are experiencing? Instead of a predetermined emotional experience or story, how can the thoughts and feelings of the audience shape the experience into something that evokes a stronger emotional connection?</p>
				<p>This project uses a dynamic system of visual music in order to create this connection. In many cases, music can combine with imagery to create a uniquely emotional experience that is difficult to experience elsewhere. Using a carefully curated library of footage, the system would creates responsive pieces of visual music to complement and affirm the mood and feelings of each individual. This piece would use the inherent visual language of mood, personal music choice, and a unique interface in order to encourage personal reflection, a process that is too often forgotten in our busy lives.</p>
