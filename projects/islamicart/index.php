				<h1>Islamic Art Application</h1>
				<h3>Spring 2013 | App Design, AfterEffects</h3>
				<p>This application was proposed to The Metropolitan Museum of Art in New York City for their Islamic Art collection. The museum needed a way to highlight certain objects in their collection for kids from 10-14 years old. These kids should be able to find these objects and learn about them in an interesting and informational manner.</p>
				<p>My team and I proposed a very stylized aesthetic for the app. Such a graphical approach to the visuals would be more appealing for the intended audience. In addition, we proposed five different, themed tours for kids that were interested in different topics. For example, the kids that were interested in buildings and architecture could specifically take a tour to see those objects.</p>
				<p>To further the appeal of the standard "tour," we proposed that the guests follow "footprints" to the next object. This serves as a fun way to navigate the museum. When a specific tour is finished, the kids "unlock" that tour and receive a badge that they can share with their friends online.</p>
