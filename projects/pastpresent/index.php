				<p>The past is the present. The present is the past.</p>
				<p>"How can we create the feeling of sifting through a box of old photos when all of the photos are digital?" It's a nostalgic interaction, but by combining it with digital technologies and services such as Instagram we can make it relevant in our digital lives.</p>
				<p>This project is an experiential web application that explores the intersection between the past and the present. It combines nostalgia and data to create a unique way to view your digital content.</p>
				<p>Past &amp; Present was created with a variety of different technologies. The experience was primarily coded in Javascript with the amazing <a href="http://threejs.org" target="_blank">Three.js</a> library. Most of the server-side code was written in PHP. MySQL databases were used to store information about the photos in the application. <a href="http://nodejs.org/" target="_blank">Node.js</a> and <a href="http://socket.io" target="_blank">Socket.io</a> were also used to handle the multi-user experience on the site.</p>
