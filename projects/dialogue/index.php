				<h1>To See &amp; Hear</h1>
				<h3>Spring 2013 | Processing, Data Visualization</h3>
				<p>For thousands of years, dialogue has been a powerful force in the world of writing. Socrates believed that truth lay within man's soul, and was to be called forth from that inner abode through the question and answer of dialogue. Although dialogue can be seen as a way to awake emotion in an audience, it also has a logical and constructive nature. It can be used as a way of seeing the overarching hierarchy and plot of a novel, as well as inform the reader of the lens in which the novel should be viewed.</p>	
				<p>Pictured here are a collection of novels that span a wide range of time, genre, and intended audience. These visualizations form their own "fingerprints" for each novel: no two are alike.</p>
				<p>Each line in these visualizations is one line of dialogue in the book. These are mapped around an inner circle, the radius of which is determined by the length of the book. Accordingly, the more dense the visual representation, the higher proportion of dialogue that exists in that specific work of literature.</p>
				<p>This project was shown at the <a href="http://remixnyc.com">RE/Mixed Media Festival IV</a> in New York in April 2014.</p>
