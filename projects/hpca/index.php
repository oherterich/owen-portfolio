				<h1>Inventing Open Societies</h1>
				<h3>2013 | System Design, UX</h3>
				<p>In January 2013, I was selected as an Open Society Foundations (OSF) Public Health Program Parsons Fellow as part of my studies while completing my MFA in Design and Technology at Parsons The New School for Design. I was to work with the Hospice and Palliative Care Association of South Africa (HPCA) during my time as a fellow.</p>
				<p>In spring 2013 in the Inventing Open Societies course held at Parsons The New School for Design my team and I designed and created a project that uses SMS to provide a simple and effective way to extend the reach of palliative care training and practice for traditional healers in South Africa.</p>
				<p>During summer 2013, I traveled to South Africa to implement and test the system we had built. The system uses Twilio to receive and process messages, while a custom PHP script interprets the SMS messages and sends to correct response to the request. The project is currently being used by an intial group of twenty traditional healers, with plans to expand to other regions and languages.</p>
